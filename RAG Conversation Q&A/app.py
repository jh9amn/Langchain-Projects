import streamlit as st
from langchain_classic.chains import create_retrieval_chain
from langchain_classic.chains.combine_documents import create_stuff_documents_chain
from langchain_classic.chains import create_history_aware_retriever
from langchain_core.chat_history import BaseChatMessageHistory
from langchain_chroma import Chroma
from langchain_community.chat_message_histories import ChatMessageHistory
from langchain_core.prompts import ChatPromptTemplate, MessagesPlaceholder
from langchain_groq import ChatGroq
from langchain_huggingface import HuggingFaceEmbeddings
from langchain_core.runnables.history import RunnableWithMessageHistory
from langchain_text_splitters import RecursiveCharacterTextSplitter
import tempfile
from langchain_community.document_loaders import (
    PyPDFLoader, 
    TextLoader, 
    UnstructuredImageLoader, 
    WebBaseLoader
)
import os
from dotenv import load_dotenv

load_dotenv()

os.environ['HF_TOKEN'] = os.getenv("HF_TOKEN")

embedddings = HuggingFaceEmbeddings(
    model_name="all-MiniLM-L6-v2",
    model_kwargs={'device': 'cpu'}
)

## Set Up streamlit App
st.title("Conversational RAG App with chat History")
st.write("Upload pdf's and chat with their content")

## Input the GROQ API Key
api_key = st.text_input("Enter your GROQ Api Key:", type="password")

## llama-3.1-8b-instant
## moonshotai/kimi-k2-instruct
## whisper-large-v3
## openai/gpt-oss-20b

## Check if groq api key provided..
if api_key:
    llm = ChatGroq(groq_api_key=api_key, model_name="llama-3.1-8b-instant")
    
    ## Chat interface
    session_id = st.text_input("Session ID", value="default_session")
    
    ## Statefully manage chat history
    
    if 'store' not in st.session_state:
        st.session_state.store={}
    
    uploaded_files = st.file_uploader("Upload Documents",
                                    type=['pdf', 'txt', 'png', 'jpg', 'jpeg'],
                                    accept_multiple_files=True
    )
    # url_input = ""
    # if not uploaded_files:
    #     url_input = st.text_input("Paste a web link (e.g., https://example.com)")
    
    url_input = st.text_input("Paste a web link (e.g., https://example.com)")

    
    ## Processed uploaded PDF's
    # if uploaded_files:
    #     documents = []
    #     for uploaded_file in uploaded_files:
    #         temppdf = f"./temp.pdf"
    #         with open(temppdf, "wb") as file:
    #             file.write(uploaded_file.getvalue())
    #             file_name=uploaded_file.name
                
    #         loader = PyPDFLoader(temppdf)
    #         docs = loader.load()
    #         documents.extend(docs)
            
    
    documents = []
    
    ## Process files (pdf, text, image)
    if uploaded_files:
        for uploaded_file in uploaded_files:
            extension = uploaded_file.name.split('.')[-1].lower()
            with tempfile.NamedTemporaryFile(delete=False, suffix=f".{extension}") as tmp_file:
                tmp_file.write(uploaded_file.getvalue())
                tmp_path = tmp_file.name
                
            try:
                if extension == "pdf":
                    loader = PyPDFLoader(tmp_path)
                elif extension == "txt":
                    loader = TextLoader(tmp_path)
                elif extension in ["png", "jpg", "jpeg"]:
                    # OCR for images
                    loader = UnstructuredImageLoader(tmp_path)
                    
                documents.extend(loader.load())
            finally:
                ## clean up the temp file
                if os.path.exists(tmp_path):
                    os.remove(tmp_path)
    
    ## Process Web Links
    if url_input:
        try:
            web_loader = WebBaseLoader(url_input)
            documents.extend(web_loader.load())
        except Exception as e:
            st.error(f"Error loading URL: {e}")
    
    
    if documents:        
        # Split and create embeddings for the documents
        text_splitter = RecursiveCharacterTextSplitter(chunk_size=5000, chunk_overlap=200)
        splits = text_splitter.split_documents(documents)
        vectorstore =   Chroma.from_documents(documents=splits, embedding=embedddings)
        retriever = vectorstore.as_retriever()
        
        contextualize_q_system_prompt = (
            "Given a chat history and the latest user question"
            "which might reference context in the chat history, "
            "formulate a standalone question which can be understood "
            "without the chat history. Do NOT answer the question, "
            "just reformulate it if needed and otherwise return it as is."
        )
        
        contextualize_q_prompt = ChatPromptTemplate.from_messages(
            [
                ("system", contextualize_q_system_prompt),
                MessagesPlaceholder("chat_history"),
                ("human", "{input}"),
            ]
        )
        
        ## History retriever with memory
        history_aware_retriever = create_history_aware_retriever(llm, retriever, contextualize_q_prompt)
        
        
        ## Answer Question
        system_prompt = (
            "You are an assistant for question-answering tasks. "
            "Use the following pieces fo retrieved context to answer "
            "the question. If you don't know the answer, say that you "
            "don't know. Use three sentences maximum and keep the "
            "answer concise."
            "\n\n"
            "{context}"
        )
        
        qa_prompt = ChatPromptTemplate.from_messages([
            ("system", system_prompt),
            MessagesPlaceholder("chat_history"),
            ("human", "{input}"),
        ])
        
        question_answer_chain = create_stuff_documents_chain(llm, qa_prompt)
        rag_chain = create_retrieval_chain(history_aware_retriever, question_answer_chain)
        
        def get_session_history(session:str) -> BaseChatMessageHistory:
            if session_id not in st.session_state.store:
                st.session_state.store[session_id] = ChatMessageHistory()
            return st.session_state.store[session_id]
        
        
        conversational_rag_chain = RunnableWithMessageHistory(
            rag_chain, 
            get_session_history,
            input_messages_key="input",
            history_messages_key="chat_history",
            output_messages_key="answer"
        )
    
        user_input = st.text_input("Your Question:")
        if user_input:
            session_history = get_session_history(session_id)
            response = conversational_rag_chain.invoke(
                {"input": user_input},
                config={
                    "configurable": {"session_id": session_id}
                } ## construct a key "abc123" in store
            )
            
            st.write(st.session_state.store)
            st.write("Assistance:", response["answer"])
            st.write("Chat History:", session_history.messages)
        else:
            st.warning("Please enter the GROQ Api key")